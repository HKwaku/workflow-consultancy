-- ---------- Hybrid search RPC ----------
-- Combines cosine similarity + FTS rank with reciprocal rank fusion (RRF).
-- Callers pass deal_id (RLS-bounded), the query embedding, and the raw query
-- text for keyword scoring. Returns chunks ordered by fused score.
CREATE OR REPLACE FUNCTION public.search_deal_chunks(
  p_deal_id      uuid,
  p_query_text   text,
  p_query_vector vector(1024) DEFAULT NULL,
  p_limit        integer      DEFAULT 12,
  p_party        text         DEFAULT NULL
)
RETURNS TABLE (
  chunk_id       uuid,
  document_id    uuid,
  filename       text,
  page_number    integer,
  slide_number   integer,
  sheet_name     text,
  cell_range     text,
  section_path   text,
  content        text,
  semantic_score real,
  keyword_score  real,
  fused_score    real
)
LANGUAGE sql STABLE AS $$
  WITH semantic AS (
    SELECT c.id,
           CASE
             WHEN p_query_vector IS NOT NULL AND c.embedding IS NOT NULL
               THEN 1 - (c.embedding <=> p_query_vector)
             ELSE 0
           END                                                  AS score,
           ROW_NUMBER() OVER (
             ORDER BY CASE
               WHEN p_query_vector IS NOT NULL AND c.embedding IS NOT NULL
                 THEN c.embedding <=> p_query_vector
               ELSE 1
             END
           )                                                    AS rnk
      FROM public.deal_document_chunks c
      JOIN public.deal_documents d ON d.id = c.document_id
     WHERE c.deal_id = p_deal_id
       AND (p_party IS NULL OR d.source_party = p_party)
     LIMIT 50
  ),
  keyword AS (
    SELECT c.id,
           ts_rank(c.content_fts, websearch_to_tsquery('english', p_query_text)) AS score,
           ROW_NUMBER() OVER (
             ORDER BY ts_rank(c.content_fts, websearch_to_tsquery('english', p_query_text)) DESC
           ) AS rnk
      FROM public.deal_document_chunks c
      JOIN public.deal_documents d ON d.id = c.document_id
     WHERE c.deal_id = p_deal_id
       AND (p_party IS NULL OR d.source_party = p_party)
       AND c.content_fts @@ websearch_to_tsquery('english', p_query_text)
     LIMIT 50
  ),
  fused AS (
    SELECT coalesce(s.id, k.id)                          AS chunk_id,
           coalesce(s.score, 0)::real                    AS semantic_score,
           coalesce(k.score, 0)::real                    AS keyword_score,
           (
             coalesce(1.0 / (60 + s.rnk), 0)
             + coalesce(1.0 / (60 + k.rnk), 0)
           )::real                                       AS fused_score
      FROM semantic s
      FULL OUTER JOIN keyword k ON s.id = k.id
  )
  SELECT f.chunk_id,
         c.document_id,
         d.filename,
         c.page_number,
         c.slide_number,
         c.sheet_name,
         c.cell_range,
         c.section_path,
         c.content,
         f.semantic_score,
         f.keyword_score,
         f.fused_score
    FROM fused f
    JOIN public.deal_document_chunks c ON c.id = f.chunk_id
    JOIN public.deal_documents       d ON d.id = c.document_id
   ORDER BY f.fused_score DESC
   LIMIT p_limit;
$$;

COMMENT ON FUNCTION public.search_deal_chunks IS
  'Hybrid (semantic + FTS) chunk search bounded to a single deal. RRF fusion. Pass query_vector NULL to fall back to keyword-only.';

